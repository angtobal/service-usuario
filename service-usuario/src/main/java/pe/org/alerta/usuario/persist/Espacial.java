package pe.org.alerta.usuario.persist;

public class Espacial {
	private double lat;
	private double lon;
	private double elv;
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLon() {
		return lon;
	}
	public void setLon(double lon) {
		this.lon = lon;
	}
	public double getElv() {
		return elv;
	}
	public void setElv(double elv) {
		this.elv = elv;
	}
	
	}
